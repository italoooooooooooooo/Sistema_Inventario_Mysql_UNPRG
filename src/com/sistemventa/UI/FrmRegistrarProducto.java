    private void txtPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && c != '.') {
            evt.consume();
        }
        if (c == '.' && txtPrecio.getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_txtPrecioKeyTyped

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this, "¿Desea eliminar este producto?");

        if (opcion == JOptionPane.YES_OPTION) {
            try {
                int id = Integer.parseInt(txt_id.getText());

                if (ProductoLogic.eliminarProducto(id)) {
                    JOptionPane.showMessageDialog(this, "Se eliminó correctamente");
                    cargarTablaProductos();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró el producto");
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            int id = Integer.parseInt(txt_id.getText());
            String nombre = txtNombre.getText().trim();
            double precio = Double.parseDouble(txtPrecio.getText());
            int stock = Integer.parseInt(txtStock.getText());
            Categoria categoria = (Categoria) cmb_Categoria.getSelectedItem();

            Producto producto = new Producto(id, nombre, precio, stock, categoria);

            if (ProductoLogic.insertarProducto(producto)) {
                JOptionPane.showMessageDialog(this, "Se insertó correctamente");
                this.cargarTablaProductos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se puede insertar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        int fila = tblProductos.getSelectedRow();

        if (fila != -1) {
            txt_id.setText(tblProductos.getValueAt(fila, 0).toString());
            txtNombre.setText(tblProductos.getValueAt(fila, 1).toString());
            txtPrecio.setText(tblProductos.getValueAt(fila, 2).toString());
            txtStock.setText(tblProductos.getValueAt(fila, 3).toString());

            String categoriaTabla = tblProductos.getValueAt(fila, 4).toString();

            for (int i = 0; i < cmb_Categoria.getItemCount(); i++) {
                Object item = cmb_Categoria.getItemAt(i);
                Categoria categoria = (Categoria) item;
                if (categoria.toString().equals(categoriaTabla)) {
                    cmb_Categoria.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblProductosMouseClicked


    private void limpiarCampos() {
        txt_id.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");

        cmb_Categoria.setSelectedItem(null);

        txt_id.requestFocus();
    }

